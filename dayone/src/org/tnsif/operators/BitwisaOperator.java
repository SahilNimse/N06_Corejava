//Bitwise Operator - &,|, <<, >>,^.
//a << b = a * 2^b.
//a >> b = a / 2^b.
package org.tnsif.operators;

public class BitwisaOperator {

	public static void main(String[] args) {
		int x=13, y=9;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(8>>1);
		System.out.println(8<<1);
		
		

	}

}
